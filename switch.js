let grade = 'A';
switch (grade) {
    case 'A':
        console.log("Very good");
        break;
    case 'B':
        console.log("Good");
        break;
    case 'C':
        console.log("Enough");
        break;
    case 'D':
        console.log("Very Enough");
        break;
    default:
        console.log("No Grade");
        break;
}